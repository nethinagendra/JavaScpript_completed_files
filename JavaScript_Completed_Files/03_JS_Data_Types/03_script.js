//undefined
let a;
console.log(`A:${a} Type:${typeof  a}`);

//String
let empName='Indra';
console.log(`Name: ${empName} Type: ${typeof empName}`);

//Number
let empAge=27;
console.log(`Age: ${empAge} Type:${typeof empAge}`);

//Boolean
let isManager=true;
console.log(`isManager ?: ${isManager} Type: ${typeof isManager}`);

//Null
let abc=null;
console.log(`value: ${abc} Type: ${typeof abc}`);

//Re-assignment
let test=10;
test='Nagendra';
test=true;
test=null;
console.log(test);
